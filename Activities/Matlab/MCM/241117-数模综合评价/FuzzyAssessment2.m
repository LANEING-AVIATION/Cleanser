% 模糊评价矩阵
PA = [0.6 0.3 0.1 0; 0.4 0.4 0.2 0; 0.5 0.3 0.2 0];
PB = [0.5 0.4 0.1 0; 0.3 0.5 0.2 0; 0.6 0.3 0.1 0];
PC = [0.4 0.4 0.2 0; 0.2 0.5 0.3 0; 0.5 0.4 0.1 0];
% 权重向量
W = [0.4, 0.3, 0.3];
% 计算综合模糊评价向量
B_A = W * PA;  % 手机A的模糊综合评价向量
B_B = W * PB;  % 手机B的模糊综合评价向量
B_C = W * PC;  % 手机C的模糊综合评价向量
% 归一化处理
B_A = B_A / sum(B_A);
B_B = B_B / sum(B_B);
B_C = B_C / sum(B_C);
% 显示结果
fprintf('手机A的综合模糊评价向量: [%.2f, %.2f, %.2f, %.2f]\n', B_A);
fprintf('手机B的综合模糊评价向量: [%.2f, %.2f, %.2f, %.2f]\n', B_B);
fprintf('手机C的综合模糊评价向量: [%.2f, %.2f, %.2f, %.2f]\n', B_C);
% 综合评价结果比较
results = [B_A; B_B; B_C];
[max_val, best_choice] = max(results(:, 1)); % 找出综合评价结果的最高值
fprintf('最优选择是: 手机 %c\n', char('A' + best_choice - 1));
