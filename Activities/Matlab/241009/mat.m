% 定义目标函数的系数（转换为最小化问题）
f = [-2, -3, -4];  % 目标函数系数向量（最小化 -2x - 3y - 4z）

% 定义等式约束矩阵和向量
Aeq = [1.5, 3, 5; 280, 250, 400];  % 约束系数矩阵
beq = [600; 60200];  % 约束等式右边的常数向量

% 定义变量x, y, z的下界（假设变量为非负）
lb = [0, 0, 0];  % 变量的下界

% 定义整数变量（1表示x, y, z都为整数）
intcon = [1, 2, 3];  % 将x, y, z声明为整数变量

% 调用intlinprog求解
[x, fval] = intlinprog(f, intcon, [], [], Aeq, beq, lb);

% 显示结果
disp('最优整数解为：');
disp(['x = ', num2str(x(1))]);
disp(['y = ', num2str(x(2))]);
disp(['z = ', num2str(x(3))]);

disp('最大化的目标函数值为：');
disp(-fval);  % 显示最大化后的目标函数值
